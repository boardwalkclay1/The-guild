// ===============================
// ACCORDION + CINEMATIC ENGINE
// ===============================

const ruleButtons = document.querySelectorAll(".rule");
const detailsContainer = document.getElementById("rule-details");
let openRule = null;

ruleButtons.forEach(btn => {
  btn.addEventListener("click", () => {
    const id = btn.getAttribute("data-rule");

    // Close if clicking the same rule
    if (openRule === id) {
      detailsContainer.innerHTML = "";
      openRule = null;
      return;
    }

    // Replace content
    detailsContainer.innerHTML = RULES[id];
    openRule = id;

    // Re-bind glossary links
    bindGlossaryLinks();

    // Smooth scroll
    detailsContainer.scrollIntoView({ behavior: "smooth", block: "start" });
  });
});


// ===============================
// GLOSSARY LINKING ENGINE
// ===============================

function bindGlossaryLinks() {
  const terms = document.querySelectorAll(".glossary");

  terms.forEach(term => {
    term.addEventListener("click", () => {
      const target = term.getAttribute("data-term");
      // Go to glossary page with anchor
      window.location.href = `/guild/pages/glossary.html#${encodeURIComponent(target)}`;
    });
  });
}
